package CoreJAVA;

import java.util.Scanner;

public class WhileLoop {

	public static void main(String[] args) {
		
		Scanner SC = new Scanner(System.in);
		var Num = SC.nextInt();
		
		while (Num<10)
		Num++;
		Num--;
		{
			System.out.println(Num);
		    System.out.println("The Number is 10");
		    
		    System.out.println("JAVA");
		}
	}

}
